Savory Citrus Goat Cheese Tart


Ingredients:
For the tart shell:

1 ½ cups all-purpose flour
½ cup cold butter, cubed
¼ tsp salt
3-4 tbsp cold water
For the filling:

1 cup goat cheese
¼ cup crème fraîche
1 tbsp lemon zest
1 tbsp orange zest
1 tsp honey
1 tsp thyme
Salt and pepper to taste
For garnish & sauce:

3 tbsp olive oil
1 tbsp lemon juice
½ tsp Dijon mustard
Microgreens
Crispy Parmesan wafer
Instructions:
Make the tart shell:

Mix flour, butter, and salt in a bowl, then add cold water until the dough forms.
Roll it out and press it into tart molds.
Bake at 375°F (190°C) for 15 minutes.
Prepare the filling:

Blend goat cheese, crème fraîche, citrus zest, honey, thyme, salt, and pepper.
Pipe onto the cooled tart shell.
Make the sauce:

Whisk olive oil, lemon juice, and Dijon mustard to create an emulsion.
Assemble:

Drizzle sauce around the plate.
Garnish with microgreens and a crispy Parmesan wafer.</p>
